package com.akrivonos.a2chparser.pojomodel.boardmodel;

public class Icons {
    private String name;
    private float num;
    private String url;


    // Getter Methods

    public String getName() {
        return name;
    }

    public float getNum() {
        return num;
    }

    public String getUrl() {
        return url;
    }

    // Setter Methods

    public void setName(String name) {
        this.name = name;
    }

    public void setNum(float num) {
        this.num = num;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
